num1 = float(input("Enter the first number:"))
num2 = float(input("Enter the second number:"))

#Addition
result = num1 + num2
print("Addition:", float(result))

#Subtraction
result = num1 - num2
print("Subtraction:", round(float(result),2))

#Multiplication
result = num1 * num2
print("Multiplication:", float(result))

#Division
result = num1 / num2
print("Division:", round(result,2))

